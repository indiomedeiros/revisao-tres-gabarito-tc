def imprimir_nomes():
  nomes = ["João", "Maria", "Fulano", "Ciclano"]
  print("1 -", nomes[0])
  print("2 -", nomes[1])
  print("3 -", nomes[2])
  print("4 -", nomes[3])

imprimir_nomes()


def imprimir_primeiro_ultimo():
  nomes = ["João", "Maria", "Fulano", "Ciclano"]
  print("1 -", nomes[0])
  print("4 -", nomes[-1])

imprimir_primeiro_ultimo()